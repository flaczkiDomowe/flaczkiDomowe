<?php
namespace app\Orders\Handlers\Model;
use Serializable;

abstract class AbstractDomainObject implements Serializable
{
    /**
     * @var int
     */
    private $id;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }
}